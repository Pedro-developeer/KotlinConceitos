package IIBasicoControleFluxo

fun main() {

    for (i in 1..3) {
        print("$i ")
    }

    println()
    println("-------------")

    for (i in 6 downTo 0 step 2) {
        print("$i ")
    }

    println()
    println("-------------")

    for (c in "Gabriel Ferrari Ceron") {
        print("$c ")
    }

}